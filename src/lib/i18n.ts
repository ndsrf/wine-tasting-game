// Build reverse translation maps for converting Spanish/French back to English
const reverseTranslationMap: Record<string, string> = {}

// Extract all wine characteristic translations and create reverse mappings
Object.entries(resources).forEach(([lang, resource]) => {
  if (lang === 'en') return // Skip English

  const gameTranslations = resource.translation.game
  Object.entries(gameTranslations).forEach(([key, value]) => {
    if (key.startsWith('wineChar_') && typeof value === 'string') {
      // Map translated value back to English key
      const englishValue = key.replace('wineChar_', '')
      reverseTranslationMap[value.toLowerCase()] = englishValue
    }
  })
})

/**
 * Normalizes a wine characteristic value to English.
 * Handles values that are already in English, Spanish, or French.
 * @param value - The characteristic value in any language
 * @returns The English version of the characteristic
 */
export function normalizeCharacteristicToEnglish(value: string | undefined): string {
  if (!value || value === '-') return '-'

  // Check if it's already in English by trying to find it in the translation keys
  const englishKeys = Object.keys(resources.en.translation.game)
    .filter(k => k.startsWith('wineChar_'))
    .map(k => k.replace('wineChar_', ''))

  if (englishKeys.includes(value)) {
    return value // Already in English
  }

  // Try to find reverse translation
  const normalized = reverseTranslationMap[value.toLowerCase()]
  if (normalized) {
    return normalized
  }

  // If not found, return original value
  return value
}

